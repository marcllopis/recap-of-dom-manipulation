// get the element from the DOM
let classContainer = document.getElementById('classroom')
let addStudentsButton = document.getElementById('add')

let classroomToHtml = ''


const convertTagToHtml = function(array, tag) {
  let arrayToHtml = ''
  for (let i = 0; i < array.length; i++) {
    arrayToHtml += `<${tag}>${array[i]}</${tag}>`
  }
  return arrayToHtml;
}


function addStudents(arrayOfStudents) {
  // generate as many cards as students are in the students variable
  for (let i = 0; i < arrayOfStudents.length; i++) {
    classroomToHtml += `
  <div class='card-student'>
    <img src='${arrayOfStudents[i].picture}' alt='${arrayOfStudents[i].name}' class='img-student'>
    <h3>${arrayOfStudents[i].name} <i>living in ${arrayOfStudents[i].living}</i></h3>
    <p>${arrayOfStudents[i].shortAbout}</p>
    <ul>
      ${convertTagToHtml(arrayOfStudents[i].hobbies, 'li')}
    </ul>
    <button class='btn-student'>Know more</button>
  </div>
  `
  }
  classContainer.innerHTML = classroomToHtml
}



addStudentsButton.addEventListener('click', function () {
  // call the fn that adds students to the DOM
  addStudents(students)
  addStudentsButton.disabled = 'disabled'
})


